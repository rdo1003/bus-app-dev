/**
 * This program will convert a value from farenheit to celsius.
 * Author: Ryan O'Keeffe
 */

public class FToC {
    
    
    public static void main(String[] args) { 
        double f = 100.0;
        double c;
        c = (f - 32) * 5 / 9;
        System.out.printf("The temperature in celsius is %.2f/n ", + c);// printf
        
    }
    
    
}
